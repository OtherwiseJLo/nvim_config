local highlight_config = {
    enable = true,
    use_languagetree = true
  }

local incremental_selection_cofig = {
    enable = true,
    keymaps = {
      init_selection = "gnn",
      node_incremental = "grn",
      scope_incremental = "grc",
      node_decremental = "grm",
    },
  }

local refactor_config = {
    highlight_definitions = { enable = true },

-- Highlights block from current scope of cursor
    highlight_current_scope = { enable = true },

-- Smart Rename
    smart_rename = {
      enable = true,
      keymaps = {
        smart_rename = "grr",
      },
    },

-- Navigation and goto definition
    navigation = {
      enable = true,
      keymaps = {
        goto_definition = "gnd",
        list_definitions = "gnD",
        list_definitions_toc = "gO",
        goto_next_usage = "<a-*>",
        goto_previous_usage = "<a-#>",
      },
    },

-- Text object selection
    select = {
      enable = true,
      keymaps = {
        ["af"] = "@function.outer",
        ["if"] = "@function.inner",
        ["ac"] = "@class.outer",
        ["ic"] = "@class.inner",

        ["iF"] = {
          python = "(function_definition) @function",
          cpp = "(function_definition) @function",
          c = "(function_definition) @function",
          java = "(method_declaration) @function",
        },
      },
    },


-- Text object swap 
    swap = {
      enable = true,
      swap_next = {
        ["<leader>a"] = "@parameter.inner",
      },
      swap_previous = {
        ["<leader>A"] = "@parameter.inner",
      },
    },

    move = {
      enable = true,
      goto_next_start = {
        ["]m"] = "@function.outer",
        ["]]"] = "@class.outer",
      },
      goto_next_end = {
        ["]M"] = "@function.outer",
        ["]["] = "@class.outer",
      },
      goto_previous_start = {
        ["[m"] = "@function.outer",
        ["[["] = "@class.outer",
      },
      goto_previous_end = {
        ["[M"] = "@function.outer",
        ["[]"] = "@class.outer",
      },
    },


-- Text object LSP interop
    lsp_interop = {
      enable = true,
      peek_definition_code = {
        ["df"] = "@function.outer",
        ["dF"] = "@class.outer",
      },
    },
  }

require'nvim-treesitter.configs'.setup {
  indent = {enable = true},
}
  
require'nvim-treesitter.configs'.setup {
  ensure_installed = "all",
  highlight = highlight_config,
  incremental_selection = incremental_selection_cofig,
  refactor = refactor_config,
}

